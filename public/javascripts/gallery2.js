    {
        var a=document.getElementById(x);
        a.style.borderBottom='solid #ff8426 5px';
        if(x=='previous')
        {
            var b=document.getElementById('events');
            b.style.borderBottom='solid #ff8426 0';
        }
        else
        {
            var b=document.getElementById('previous');
            b.style.borderBottom='solid #ff8426 0';
        }

    }

